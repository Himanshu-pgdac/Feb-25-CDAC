class DoubleToInt {
    public static void main(String[] args) {
        double doubleValue = 123.456;
        int intValue = (int) doubleValue;
    System.out.println("Double Value: " + doubleValue);
	System.out.println("Integer Value: " + intValue);
    }
}

/* Output -
Double Value: 123.456
Integer Value: 123 */
